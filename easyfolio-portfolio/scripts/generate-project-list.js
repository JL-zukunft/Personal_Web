import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');
const projectsDir = path.join(rootDir, 'content', 'projects');
const outputFile = path.join(rootDir, 'assets', 'js', 'projects-data.js');

function generateProjectList() {
    try {
        const files = fs.readdirSync(projectsDir)
            .filter(file => file.endsWith('.md') && file !== 'README.md')
            .sort();

        const content = `// Auto-generated by scripts/generate-project-list.js
// Do not edit manually

export const projectFiles = ${JSON.stringify(files, null, 4)};
`;

        fs.writeFileSync(outputFile, content, 'utf-8');
        console.log(`Generated ${outputFile} with ${files.length} projects: ${files.join(', ')}`);
    } catch (error) {
        console.error('Failed to generate project list:', error);
        process.exit(1);
    }
}

generateProjectList();